#include <iostream>

using namespace std;

int main() {

	int x, y;

	x = 3;
	y = 9;

	cout << "O valor de x e y é: " << x << ", " << y << endl;
	cout << endl;
	cout << " A soma entre x e y é: " << x + y << endl;
	cout << " A subtração entre x e y é: " << x - y << endl;
	cout << " A multiplicação entre x e y é: " << x * y << endl;
	cout << " A divisão entre x e y é: " << y / x << endl;

	return 0;
}